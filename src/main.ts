import { NestFactory } from "@nestjs/core";
import { ValidationPipe } from "@nestjs/common";
import { AppModule } from "./app.module";
import { DocumentBuilder, SwaggerModule } from "@nestjs/swagger";
import { version } from "../package.json";
import ItemEntity from "./modules/items/entities/item.entity";
import ENV_VARIABLE_NAMES from "./utils/env_variable_names";
import { ConfigService } from "@nestjs/config";

async function bootstrap() {
    const app = await NestFactory.create(AppModule);

    app.enableCors();
    app.useGlobalPipes(new ValidationPipe({ whitelist: true }));

    const configService = app.get<ConfigService>(ConfigService);

    const swaggerConfig = new DocumentBuilder()
        .setTitle("solarwebappback")
        .setDescription(
            "Items API generated by Developers Suite Code Generator.",
        )
        .setVersion(version)
        .setExternalDoc("JSON", "/api/json")
        .addBearerAuth()
        .addSecurityRequirements("bearer");

    const swaggerServersList: string = configService.get(
        ENV_VARIABLE_NAMES.SWAGGER_SERVERS_LIST,
    );

    if (swaggerServersList) {
        swaggerServersList
            .split(",")
            .forEach((server) => swaggerConfig.addServer(server));
    }

    const document = SwaggerModule.createDocument(app, swaggerConfig.build(), {
        extraModels: [ItemEntity],
    });

    SwaggerModule.setup("api", app, document, {
        customSiteTitle: "solarwebappback",
        jsonDocumentUrl: "/api/json",
    });

    await app.listen(3001);
}
bootstrap();
